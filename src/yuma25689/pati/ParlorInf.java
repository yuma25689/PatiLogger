package yuma25689.pati;

public class ParlorInf {
	String strDateTime;
	String strTitle;
	
	ParlorInf( String datetime, String title )
	{
		strDateTime = datetime;
		strTitle = title;
	}
	public String getStrDateTime() {
		return strDateTime;
	}
	public String getStrTitle() {
		return strTitle;
	}
}
